package com.xworkz.Methods;

public class MethodType {

	//instance method
	void add() {
		int a=20;
		int b=30;
		System.out.println("adding two numbers:"+(a+b));
	}
	
	//static method
	static void sub() {
		int a=20;
		int b=30;
		System.out.println("subtracting two numbers:"+(a-b));
	}
}
